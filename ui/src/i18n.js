export function supportedLocales() {
  return Object.keys(messages)
}

export function resolveLocale(configuredLanguage = 'auto', browserLanguage = globalThis.navigator?.language) {
  const requested = String(configuredLanguage || 'auto').trim().toLowerCase()
  const language = requested === 'auto' ? browserLanguage : requested
  const [base] = String(language || DEFAULT_LOCALE).toLowerCase().split('-')
  return messages[base] ? base : DEFAULT_LOCALE
}

export function translate(locale, key, params = {}) {
  const message = messages[locale]?.[key] ?? messages[DEFAULT_LOCALE][key] ?? key
  return typeof message === 'function' ? message(params) : message
}
